package id.co.telkom.parser.entity.availmover;

public class AvailMoverModel {
	private String shema, ip, port, username, password, ktrFile;

	public String getShema() {
		return shema;
	}

	public void setShema(String shema) {
		this.shema = shema;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getPort() {
		return port;
	}

	public void setPort(String port) {
		this.port = port;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getKtrFile() {
		return ktrFile;
	}

	public void setKtrFile(String ktrFile) {
		this.ktrFile = ktrFile;
	}
	
}
